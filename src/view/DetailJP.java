     private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblChiTiet.getModel();
        model.setRowCount(0);
        List<ChitietSPEntity> list = ctDAO.getAll();
        for (ChitietSPEntity ct : list) {
            Object[] data = {
                ct.getMaNoiThat(),
                ct.getMaLoai(),
                ct.getTenNoiThat(),
                ct.getMoTa(),
                ct.getGia(),
                ct.getLinkDenAnh()
            };
            model.addRow(data);
        }
    }
     private void loadMaLoai() {
        LoaiDAO loaiDAO = new LoaiDAO();
        List<Long> dsMaLoai = loaiDAO.getAllMaLoai();
        cboMaLoai.removeAllItems();
        for (Long ma : dsMaLoai) {
            cboMaLoai.addItem(String.valueOf(ma));
        }
    }
       private ChitietSPEntity getChiTiet() {
    try {
        long maNoiThat = Long.parseLong(txtMaNoiThat.getText().trim());
        long maLoai = Long.parseLong(cboMaLoai.getSelectedItem().toString());
        String ten = txtTen.getText().trim();
        String moTa = txtMoTa.getText().trim();
        int gia = Integer.parseInt(txtGia.getText().trim());
        String link = txtLink.getText().trim();

        return new ChitietSPEntity(maNoiThat, maLoai, ten, moTa, gia, link);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi nhập liệu khi thêm/sửa: " + e.getMessage());
        return null;
    }
}

